#include<stdio.h>
int main()
{
    int T;
    long long int n,M[2][2],F[2][2],a=0,b=0,c=0,d=0,k=0,nn,fn=0,fnn=0;
    scanf("%d",&T);
    k=1000000007;
    while(T--)
    {
        scanf("%lld",&n);
        nn=n+1;
        fn=0;
        fnn=0;
        F[0][0]=1;
        F[0][1]=1;
        F[1][0]=1;
        F[1][1]=0;

        M[0][0]=1;
        M[0][1]=0;
        M[1][0]=0;
        M[1][1]=1;
        while(n)
        {
            if(n%2!=0)
            {
                a=((F[0][0]*M[0][0])+(F[1][0]*M[0][1]))%k;
                b=((F[0][1]*M[0][0])+(F[1][1]*M[0][1]))%k;
                c=((M[1][0]*F[0][0])+(M[1][1]*F[1][0]))%k;
                d=((M[1][0]*F[0][1])+(M[1][1]*F[1][1]))%k;
                M[0][0]=a;
                M[0][1]=b;
                M[1][0]=c;
                M[1][1]=d;
            }

            a=((F[0][0]*F[0][0])+(F[0][1]*F[1][0]))%k;
            b=((F[0][0]*F[0][1])+(F[1][0]*F[1][1]))%k;
            c=((F[1][0]*F[0][0])+(F[1][1]*F[1][0]))%k;
            d=((F[1][0]*F[0][1])+(F[1][1]*F[1][1]))%k;

            F[0][0]=a;
            F[0][1]=b;
            F[1][0]=c;
            F[1][1]=d;

            n=n>>1;
        }
        fn=M[0][0]%k;

        F[0][0]=1;
        F[0][1]=1;
        F[1][0]=1;
        F[1][1]=0;

        M[0][0]=1;
        M[0][1]=0;
        M[1][0]=0;
        M[1][1]=1;

        while(nn)
        {
            if(nn%2!=0)
            {
                a=((F[0][0]*M[0][0])+(F[1][0]*M[0][1]))%k;
                b=((F[0][1]*M[0][0])+(F[1][1]*M[0][1]))%k;
                c=((M[1][0]*F[0][0])+(M[1][1]*F[1][0]))%k;
                d=((M[1][0]*F[0][1])+(M[1][1]*F[1][1]))%k;
                M[0][0]=a;
                M[0][1]=b;
                M[1][0]=c;
                M[1][1]=d;
            }

            a=((F[0][0]*F[0][0])+(F[0][1]*F[1][0]))%k;
            b=((F[0][0]*F[0][1])+(F[1][0]*F[1][1]))%k;
            c=((F[1][0]*F[0][0])+(F[1][1]*F[1][0]))%k;
            d=((F[1][0]*F[0][1])+(F[1][1]*F[1][1]))%k;

            F[0][0]=a;
            F[0][1]=b;
            F[1][0]=c;
            F[1][1]=d;

            nn=nn>>1;
        }
        fnn=M[0][0]%k;
        printf("$%lld\n",(fn*fnn)%k-1);
    }
    return 0;
}
